public class DoWhileStat {
    public static void main(String[] args) {
        System.out.println("-----Software starts-------");
        int i = 1;
        do{
            System.out.println("Hi");
            System.out.println("Hello");
            i++;
        }while(i<=5);
        System.out.println("-----Software closed-------");
         
    }
}
